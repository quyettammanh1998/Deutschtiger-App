List<JourneyLesson> _generateLessons(String chapterId, String level, int total) {
  final lessonTypes = ['vocabulary', 'grammar', 'dialogue', 'exercise', 'review'];
  final lessonTitles = {
    'A1': ['Greetings', 'Numbers 1-20', 'Colors', 'Family', 'Food & Drinks', 'At Home', 'Daily Routine', 'Shopping', 'Time & Dates', 'Weather', 'Body Parts', 'Clothing', 'Transportation', 'At the Doctor', 'Leisure Time', 'Directions', 'Phone Calls', 'Asking for Help', 'Simple Past', 'Review A1'],
    'A2': ['Past Tense', 'Modal Verbs', 'Reflexive Verbs', 'Prepositions', 'Cases Review', 'Comparisons', 'Future Tense', 'Opinions', 'Requests', 'Invitations', 'Describing People', 'At Work', 'Travel Planning', 'Renting', 'Insurance', 'Health Issues', 'Environmental Topics', 'Media', 'Culture', 'Review A2'],
    'B1': ['Subjunctive II', 'Passive Voice', 'Relative Clauses', 'Causative', 'Nominalization', 'Compound Nouns', 'Infinitive Clauses', 'Opinion Essays', 'Job Applications', 'News & Media', 'History', 'Science Topics', 'Art & Literature', 'Social Issues', 'Travel Stories', 'Relationships', 'Career Development', 'Financial Topics', 'Political Issues', 'Review B1'],
    'B2': ['Complex Sentences', 'Formal Register', 'Academic Writing', 'Business German', 'Scientific German', 'Literary Analysis', 'Cultural Criticism', 'Philosophical Concepts', 'Legal Texts', 'Technical German', 'Media Analysis', 'Social Commentary', 'Historical Analysis', 'Psychological Terms', 'Economic Concepts', 'Environmental Policy', 'Immigration Topics', 'European Union', 'Global Issues', 'Review B2'],
    'C1': ['Academic Discourse', 'Abstract Concepts', 'Critical Analysis', 'Rhetorical Devices', 'Linguistic Nuances', 'Stylistic Variation', 'Literary Criticism', 'Advanced Grammar', 'Register Shifts', 'Metaphorical Language', 'Argumentative Writing', 'Persuasive Techniques', 'Text Analysis', 'Discourse Analysis', 'Professional Communication', 'Negotiation', 'Presentation Skills', 'Research Methods', 'Cultural Contexts', 'Review C1'],
    'C2': ['Idiomatic Expressions', 'Regional Variations', 'Historical Texts', 'Philosophical Texts', 'Journalistic Excellence', 'Literary Creation', 'Translation Practice', 'Interpreting', 'Public Speaking', 'Debate Mastery', 'Essay Excellence', 'Creative Writing', 'Advanced Syntax', 'Semantic Complexity', 'Discourse Mastery', 'Cultural Mastery', 'Professional Excellence', 'Academic Mastery', 'Native Mastery', 'Review C2'],
  };

  return List.generate(total, (i) {
    final typeIndex = i % lessonTypes.length;
    final titleList = lessonTitles[level] ?? [];
    final title = i < titleList.length ? titleList[i] : 'Lesson ${i + 1}';
    
    return JourneyLesson(
      id: '$chapterId-lesson-${i + 1}',
      chapterId: chapterId,
      title: title,
      titleVi: title,
      description: 'Learn $title at $level level',
      descriptionVi: 'Học $title cấp độ $level',
      type: lessonTypes[typeIndex],
      order: i + 1,
      durationMinutes: 10 + (i * 2) % 20,
      xpReward: 10 + (i * 2),
      isCompleted: i < 3,
      isLocked: i >= 3,
    );
  });
}

