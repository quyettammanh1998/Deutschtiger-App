final _fixtureMarker = RegExp(
  r'\b(?:mock\w*|fixture\w*|placeholder\w*)\b',
  caseSensitive: false,
);

void main() {
  test(
    'release-visible route screens do not reference mock or fixture data',
    () {
      for (final sourcePath in _releaseVisibleRouteSources) {
        final source = File(sourcePath).readAsStringSync();
        final markers = _fixtureMarker
            .allMatches(source)
            .map((match) => match.group(0))
            .toSet();

        expect(
          markers,
          isEmpty,
          reason:
              '$sourcePath is release-visible. Move test/demo content out of '
              'the route or gate the route behind a disabled release flag.',
        );
      }
    },
  );
}
